    public VideoDeviceInfo(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        VideoDevice[] devices = VideoDevice.getDevices();
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("Device");
        model.addColumn("Name");
        model.addColumn("Version");
        model.addColumn("Type");
        String[] line = new String[4];
        for (VideoDevice d : devices){
            line[0] = d.getFile().getAbsolutePath();
            line[1] = d.getName();
            line[2] = d.getVersion().name();
            line[3] = d.getType().name();
            model.addRow(line);

        }
        tableInfo.setModel(model);
        tableInfo.getColumnModel().getColumn(1).setMinWidth(200);
    }

