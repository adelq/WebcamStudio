    @Override
    public void startSaving(String studio, int itemCount) {
        totalItemCount = itemCount;
        lblStudioName.setText(studio);
        pgBarTotal.setMaximum(itemCount);
        pgBarTotal.setMinimum(0);
        pgBarTotal.setValue(0);
        pgBarFile.setMaximum(100);
        pgBarFile.setMinimum(0);
        pgBarFile.setValue(0);
        pgBarFile.setString("");
    }

    @Override
    public void startLoading(String studio, int itemCount) {
        totalItemCount = itemCount;
        lblStudioName.setText(studio);
        pgBarTotal.setMaximum(itemCount);
        pgBarTotal.setMinimum(0);
        pgBarTotal.setValue(0);
        pgBarFile.setMaximum(100);
        pgBarFile.setMinimum(0);
        pgBarFile.setValue(0);
        pgBarFile.setString("");
        pack();
    }

    @Override
    public void unCompressing(String file, long size, long currentPos, int itemIndex) {
        pgBarFile.setValue((int)(currentPos*100/size));
        pgBarFile.setString(file);
        pgBarTotal.setValue(itemIndex*100/totalItemCount);
    }

    @Override
    public void compressing(String file, long size, long currentPos, int itemIndex) {
        pgBarFile.setValue((int)(currentPos*100/size));
        pgBarFile.setString(file);
        pgBarTotal.setValue(itemIndex*100/totalItemCount);
    }

    @Override
    public void completedSaving(String studio) {
       dispose();
    }

    @Override
    public void completedLoading(String studio) {
        dispose();
    }

